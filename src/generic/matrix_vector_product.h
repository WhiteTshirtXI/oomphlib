//Include guards
#ifndef OOMPH_MATRIX_VECTOR_PRODUCT_HEADER
#define OOMPH_MATRIX_VECTOR_PRODUCT_HEADER


// Config header generated by autoconfig
#ifdef HAVE_CONFIG_H
#include <oomph-lib-config.h>
#endif

#include "matrices.h"
#include "linear_algebra_distribution.h"
#ifdef HAVE_TRILINOS
#include "trilinos_helpers.h"
#endif

namespace oomph {


//=============================================================================
/// \short Matrix vector product helper class - primarily a wrapper to 
/// Trilinos's Epetra matrix vector product methods. This allows the 
/// epetra matrix to be assembled once and the matrix vector product to be
/// performed many times.
//=============================================================================
 class MatrixVectorProduct : public DistributableLinearAlgebraObject
  {

    public:
   
   /// \short Constructor
   MatrixVectorProduct()
    {
     // null pointers
#ifdef HAVE_TRILINOS
     Epetra_matrix_pt = 0;
     Epetra_range_map_pt = 0;
#ifdef OOMPH_HAS_MPI
     Global_rows = 0;
#endif
     Epetra_domain_map_pt = 0;
#ifdef OOMPH_HAS_MPI
     Global_cols = 0;
#endif
     Epetra_comm_pt = 0;
     Epetra_col_map_pt = 0;
#endif
     Oomph_matrix_pt = 0;
     Column_distribution_pt = 0;
    }

   /// Broken copy constructor
   MatrixVectorProduct(const MatrixVectorProduct&) 
    { 
     BrokenCopy::broken_copy("MatrixVectorProduct");
    } 
 
   /// Broken assignment operator
   void operator=(const MatrixVectorProduct&) 
    {
     BrokenCopy::broken_assign("MatrixVectorProduct");
    }

   /// \short Destructor
   ~MatrixVectorProduct()
    {
     this->clean_up_memory();
    }

   /// \short clear the memory
   void clean_up_memory()
    {
#ifdef HAVE_TRILINOS
     delete Epetra_matrix_pt;
     delete Epetra_range_map_pt;
     Epetra_matrix_pt = 0;
     Epetra_range_map_pt = 0;
#ifdef OOMPH_HAS_MPI
     delete[] Global_rows;
     Global_rows = 0;
#endif
     delete Epetra_domain_map_pt;
     Epetra_domain_map_pt = 0;
#ifdef OOMPH_HAS_MPI
     delete[] Global_cols;
     Global_cols = 0;
#endif
     delete Epetra_comm_pt;
     delete Epetra_col_map_pt;
     Epetra_comm_pt = 0;
     Epetra_col_map_pt = 0;
#endif
     delete Oomph_matrix_pt;
     Oomph_matrix_pt = 0;
     delete Column_distribution_pt;
     Column_distribution_pt = 0;
    }

   /// \short Setup the matrix vector product operator.\n
   /// WARNING: This class is wrapper to Trilinos Epetra matrix vector
   /// multiply methods, if Trilinos is not installed then this class will
   /// function as expected, but there will be no computational speed gain.\n
   /// By default the Epetra_CrsMatrix::multiply(...) are employed.\n
   void setup(CRDoubleMatrix* matrix_pt);

   /// \short Apply the operator to the vector x and return the result in 
   /// the vector y
   void multiply(const DoubleVector& x, DoubleVector& y);

   /// \short Apply the transpose of the operator to the vector x and return 
   /// the result in the vector y
   void multiply_transpose(const DoubleVector& x, DoubleVector& y);

   /// Access function to the number of columns.
   const unsigned& ncol() const 
    {
     return Ncol;
    }

    private:

   /// Helper function to setup for oomph-lib method
   void setup_oomph_method_helper(CRDoubleMatrix* matrix_pt);

#ifdef HAVE_TRILINOS
   /// Helper function for multiply(...)
   void multiply_helper(const DoubleVector& x, DoubleVector& y);

   /// Helper function for multiply_transpose(...)
   void multiply_transpose_helper(const DoubleVector& x, DoubleVector& y);
#endif

#ifdef HAVE_TRILINOS
   /// \short The Epetra version of the matrix
   Epetra_CrsMatrix* Epetra_matrix_pt;

   /// Epetra_Map for the rows of the matrix (equivalent to Distribution_pt)
   Epetra_Map* Epetra_range_map_pt;

#ifdef OOMPH_HAS_MPI   
   /// List of global rows in the Epetra_range_map_pt
   int* Global_rows;
#endif

   /// Epetra_map for the domain. (Equivalent to Column_distribution_pt)
   Epetra_Map* Epetra_domain_map_pt;

#ifdef OOMPH_HAS_MPI
   /// List of global indices in the Epetra_domain_map_pt
   int* Global_cols;
#endif

   /// An Epetra_map for the columns of the matrix
   Epetra_Map* Epetra_col_map_pt;

#ifdef OOMPH_HAS_MPI
   /// Epetra communicator (mpi version)
   Epetra_MpiComm* Epetra_comm_pt;
#else
   /// Epetra communicator (serial version)
   Epetra_SerialComm* Epetra_comm_pt;
#endif

#endif

   /// \short an oomph-lib matrix
   CRDoubleMatrix* Oomph_matrix_pt;

   /// \short The (assumed) distribution of: x if using multiply(...) or y
   /// if using multiply_transpose(...) where this is A x = y.
   LinearAlgebraDistribution* Column_distribution_pt;

   /// The number of columns (assuming not transposed)
   unsigned Ncol;
  };
}
#endif
